		</p>
	</section>
	<section class="infographic-section">
		<h2>Historical Background</h2>
		<p>
			The Ghats consist of steps leading to the River Ganges, thus sharing its historical and
			religious importance. There are 89 Ghats in total.
		</p>
	</section>
	<section class="infographic-section">
		<h2>Cultural and Religious Significance</h2>
		<p>
			As the pathway to the Ganges River, the Ghats play a very significant role in the Hindu
			religion, similar to the River Ganges itself. For example, "the Ghats of Varanasi are
			considered one of the auspicious locations" for cremation, one of the rites of passage or
			rituals in the Hindu religion.
		</p>
	</section>
	<section class="infographic-section">
		<h2>Human-Environment Interaction</h2>
		<p>
			As stairs, the construction of the Ghats required environmental modifications. Additionally,
			it is used by humans to access the river for various activities mentioned above.
		</p>
	</section>
	<section class="infographic-section">
		<h2>Current Issues</h2>
		<ul>
			<li>Pollution caused by cremation rituals and other activities.</li>
