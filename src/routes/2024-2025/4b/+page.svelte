				>
			</li>
		</ul>
	</section>
	<section class="infographic-section">
		<h2>Facts</h2>
		<ul>
			<li>
				Hindus believe ashes laid in a sacred place like the Ganges increase chances of achieving
				moksha.
			</li>
			<li>The Dashashwamedh Ghat is the most popular Ghat.</li>
			<li>
				Brahma Ghat hosts the headquarters of the Sri Kashi Math Samsthan, followed by
				Konkani-speaking Goud Saraswat Brahmins.
			</li>
